//Write your Constructor Function that will generate a
//different response for each day of the week.

var object = function() {
  var _days = function(day) {
    if (day === 'Monday') {
      return 'That sucks!';
    } else if (day === 'Tuesday') {
      return 'A little better...';
    } else if (day === 'Wednesday') {
      return 'Hump Day!';

    } else if (day === 'Thursday') {
      return 'So close!';

    } else if (day === 'Friday') {
      return 'Time for a beer!';

    } else if (day === 'Saturday') {
      return 'Time for another beer!';

    } else if (day === 'Sunday') {
      return 'Time for some football and a beer!';
    }
  };
  return {
    getDay: function(day) {
      return object._days(day);
    }
  };

};

module.exports = getDay();
